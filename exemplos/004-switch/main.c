/* switch */

#include <stdio.h>

void main ()
{
    int a = 3;

    switch (a)
    {
        case 1:
            printf("a = 1\n");
            break;
        case 2:
            printf("a = 2\n");
            break;
        case 3:
            printf("a = 3\n");
            break;
        default:
            printf("???\n");
    }
}
